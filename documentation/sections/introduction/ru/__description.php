<h2 class="item1">Введение</h2>

<h5>
    Данный скрипт позволяет легко и быстро вставлять видео, как в качестве фона, так и в качестве видеоплеера на страницу
    вашего шаблона. Особенностью данного скрипта является адаптация качества видео под скорость интернета и производительность
    компьютера.
</h5>

